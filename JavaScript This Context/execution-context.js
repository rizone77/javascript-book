// Execution Context কী জিনিস? এইটা কীভাবে কাজ করে?

// **Execution Context** হলো JavaScript কোড চালানোর সময়ের পরিবেশ। এটা নির্ধারণ করে কোড কিভাবে এক্সিকিউট হবে।

// ### 3 ধরনের Execution Context:

// 1. **Global Execution Context (GEC)**: কোডের প্রাথমিক কনটেক্সট।
// 2. **Function Execution Context (FEC)**: ফাংশন কল করার সময় তৈরি হয়।
// 3. **Eval Execution Context**: `eval()` ব্যবহারে তৈরি হয় (কম ব্যবহৃত)।

// ### কাজের প্রক্রিয়া:

// 1. **Hoisting**: ভেরিয়েবল এবং ফাংশন **hoisted** হয়।
// 2. **Execution Phase**: কোড এক্সিকিউট হয় এবং মান অ্যাসাইন হয়।


// গ্লোবাল কনটেক্সটের এর কাজ কী? এইটা কেন দরকার পড়ে?

// **গ্লোবাল কনটেক্সট** হলো যখন কোডের বাইরে কোনো ফাংশন বা ব্লক না থাকলে কোড এক্সিকিউট হয়। গ্লোবাল কনটেক্সটে ডিক্লেয়ার করা ভেরিয়েবল ও ফাংশন সারা স্ক্রিপ্টে এক্সেস করা যায়।

// ### কেন দরকার:

// * **ভেরিয়েবল/ফাংশন শেয়ারিং**: এক জায়গায় ডিক্লেয়ার করে সব জায়গায় ব্যবহার করা যায়।
// * **অ্যাপ্লিকেশন স্টেট**: গ্লোবাল ভেরিয়েবল অ্যাপ্লিকেশনের গুরুত্বপূর্ণ তথ্য রাখতে সাহায্য করে।
